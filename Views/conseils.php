    <h1>Nos conseils pour économiser l'eau</h1>
    
    <div class="topic__wrapper">
        <h2>Dans la salle de bain :</h2>
        <div>
            <img src="Public/medias/images/bathroom.jpg" alt="salle de bain">
            <ol>
                <li>Optez pour des douches plutôt que des bains, cela peut économiser des centaines de litres d'eau par semaine.</li>
                <li>Installez des pommes de douche à faible débit pour réduire la consommation d'eau pendant la douche.</li>
                <li>Réparez rapidement les fuites de robinets et de toilettes pour éviter le gaspillage d'eau.</li>
            </ol>
        </div>
    </div>

    <div class="topic__wrapper">
        <h2>Dans la cuisine :</h2>
        <div>
            <img src="Public/medias/images/kitchen.jpg" alt="cuisine">
            <ol>
                <li>Remplissez le lave-vaisselle avant de le faire fonctionner pour optimiser son utilisation.</li>
                <li>Lavez les fruits et légumes dans un récipient rempli d'eau plutôt que sous un jet d'eau courante.</li>
                <li>Utilisez un évier bouché ou un récipient pour laver la vaisselle à la main plutôt que de laisser l'eau couler.</li>
            </ol>
        </div>
    </div>

    <div class="topic__wrapper">
        <h2>Dans la buanderie :</h2>
        <div>
            <img src="Public/medias/images/buanderie.jpg" alt="buanderie">
            <ol>
                <li>Attendez d'avoir une pleine charge avant de faire fonctionner la machine à laver.</li>
                <li>Utilisez des cycles de lavage courts et à faible consommation d'eau lorsque cela est possible.</li>
                <li>Réutilisez l'eau de rinçage pour arroser les plantes ou nettoyer les sols.</li>
            </ol>
        </div>
    </div>

    <div class="topic__wrapper">
        <h2>Autres astuces :</h2>
        <div>
            <img src="Public/medias/images/astuces.jpg" alt="astuces">
            <ol>
                <li>Collectez l'eau de pluie pour arroser votre jardin.</li>
                <li>Installez des robinets à débit réduit dans votre maison pour limiter la quantité d'eau utilisée.</li>
                <li>Encouragez tous les membres de la famille à adopter des habitudes économes en eau.</li>
            </ol>
        </div>
    </div>

    <div class="topic__wrapper conclusion">
        <h2>En conclusion</h2>
       <p>La préservation de l'eau est une responsabilité que nous partageons tous. En prenant conscience de notre consommation et en adoptant des pratiques économes en eau, nous pouvons contribuer à préserver cette ressource vitale pour les générations futures. Chaque goutte compte, et chaque geste que nous faisons pour économiser l'eau dans notre foyer fait une différence. Ensemble, engageons-nous à être des gardiens de l'eau et à faire de petits changements qui auront un impact significatif sur notre planète et sur notre avenir.</p>
    </div>
