    <h1>Politique de confidentialité</h1>

    <p>Merci d'utiliser notre application web pour calculer votre consommation d'eau annuelle et pour obtenir des conseils sur la manière d'économiser cette précieuse ressource. Chez nous, la confidentialité de vos données est une priorité absolue. Cette politique de confidentialité explique comment nous collectons, utilisons et protégeons vos informations lorsque vous utilisez notre application.</p>

    <h2>Collecte et utilisation des informations</h2>

    <p>Nous ne collectons aucune donnée personnelle via l'application pour le calcul de la consommation d'eau. Aucune donnée saisie par l'utilisateur n'est enregistrée dans une base de données. Toutes les informations que vous fournissez restent strictement confidentielles et ne sont pas stockées ou utilisées à des fins autres que le calcul de votre consommation d'eau.</p>

    <h2>Formulaire de contact</h2>

    <p>Si vous choisissez de nous contacter via le formulaire de contact sur notre site web, nous collecterons votre adresse e-mail ainsi que le message que vous nous envoyez. Ces informations seront utilisées uniquement pour répondre à votre demande et pour vous fournir un service de qualité. Nous ne partagerons pas vos informations avec des tiers sans votre consentement explicite, sauf si cela est requis par la loi.</p>

    <h2>Protection des données</h2>

    <p>Nous prenons des mesures de sécurité appropriées pour protéger vos informations contre tout accès non autorisé, altération, divulgation ou destruction. Cela inclut des contrôles de sécurité physiques, électroniques et procéduraux pour protéger vos données personnelles.</p>

    <h2>Cookies</h2>

    <p>Nous n'utilisons pas de cookies pour collecter des informations personnelles vous concernant lorsque vous utilisez notre application.</p>

    <h2>Modifications de la politique de confidentialité</h2>

    <p>Nous nous réservons le droit de mettre à jour cette politique de confidentialité à tout moment. Toute modification sera publiée sur cette page, et la date de révision en haut de la page sera mise à jour. Nous vous encourageons à consulter régulièrement cette politique de confidentialité pour rester informé de la manière dont nous protégeons vos informations.</p>

    <h2>Contactez-nous</h2>

    <p>Si vous avez des questions concernant notre politique de confidentialité ou nos pratiques en matière de confidentialité, veuillez nous contacter via le formulaire de contact sur notre site web.</p>
